        playerLabel[0] = firstPic;
        playerLabel[1] = secondBtn;
        playerLabel[2] = thirdBtn;
        playerLabel[3] = fourthBtn;
        playerLabel[4] = fifthBtn;

        playerPoint += DeckOfCards.value(cards.get(numPlayer));

