    private void stayBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stayBtnActionPerformed

            dealerLabel[0] = dealerBtn1;
            dealerLabel[1] = dealerBtn2;
            dealerLabel[2] = dealerBtn3;
            dealerLabel[3] = dealerBtn4;
            dealerLabel[4] = dealerBtn5;
        do {
            
            dealerPoint += DeckOfCards.value(cards.get(numDealer));

            System.out.println(dealerPoint);

            dealerLabel[labeldealer].setIcon(image[cards.get(numDealer) - 1]);
            dealerLabel[labeldealer].setBorder(javax.swing.BorderFactory.createBevelBorder(javax.swing.border.BevelBorder.RAISED, new java.awt.Color(183, 183, 164), new java.awt.Color(183, 183, 164), new java.awt.Color(183, 183, 164), new java.awt.Color(183, 183, 164)));
            try {
                    Thread.sleep(1000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(OflflineMode.class.getName()).log(Level.SEVERE, null, ex);
                }
            jLabel5.setText("dealer point =" + dealerPoint);
            jLabel6.setText("last card point(dealer) =" + Integer.toString(DeckOfCards.value(cards.get(numDealer))));

            if (labeldealer < 5) {
                labeldealer++;
                numDealer++;
            }
                
        } while (playerPoint >= dealerPoint);
        
        
        if (dealerPoint > 21) {
            winOrLose.setForeground(new java.awt.Color(128, 255, 219));
            winOrLose.setText("you won:)");
            hitBtn.setEnabled(false);
            stayBtn.setEnabled(false);
            restartBtn.setEnabled(true);
        } else if (playerPoint <= dealerPoint) {
            winOrLose.setForeground(new java.awt.Color(208, 0, 0));
            winOrLose.setText("you Lose:(");
            hitBtn.setEnabled(false);
            stayBtn.setEnabled(false);
            restartBtn.setEnabled(true);
        } else {
            winOrLose.setForeground(new java.awt.Color(128, 255, 219));
            winOrLose.setText("you won:)");
            hitBtn.setEnabled(false);
            stayBtn.setEnabled(false);
            restartBtn.setEnabled(true);
        }

    }//GEN-LAST:event_stayBtnActionPerformed

